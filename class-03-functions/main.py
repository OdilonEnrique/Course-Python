def my_sum(a, b):
    total = a + b
    return total

print(my_sum(2, 3))
print(my_sum(5, 6))
print(my_sum(10, 20))

def my_sub(a, b):
    total = a - b
    return total

print(my_sub(2, 3))
print(my_sub(5, 6))
print(my_sub(10, 20))

def calc_avg(grade1, grade2):
    avg = (grade1 + grade2) / 2
    return avg

print(calc_avg(10, 6))